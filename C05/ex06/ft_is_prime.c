int	ft_is_prime(int nb)
{
	int r;

	if (nb <= 1)
		return (0);
	r = 2;
	while (nb % r)
	{
		if (r > nb)
			break ;
		r++;
	}
	if (r == nb)
		return (1);
	return (0);
}
