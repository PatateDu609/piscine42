void	ft_rev_int_tab(int *tab, int size)
{
	int temp[size];
	int i;

	i = -1;
	while (++i < size)
		temp[i] = tab[i];
	i = -1;
	while (++i < size)
		tab[i] = temp[size - i - 1];
}
